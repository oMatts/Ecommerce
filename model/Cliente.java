package model;

/**
 *
 * @author Matt
 */
public class Cliente {
    private String nome;
    private String email;
    private int salario;
    private int id;

    public Cliente(String nome, String email, int salario, int id) {
        this.nome = nome;
        this.email = email;
        this.salario = salario;
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getSalario() {
        return salario;
    }

    public void setSalario(int salario) {
        this.salario = salario;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

}
